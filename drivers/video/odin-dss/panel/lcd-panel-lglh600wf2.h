#define LONG_CMD_MIPI   0
#define SHORT_CMD_MIPI  1
#define DSI_DELAY	2

#define SP(dt, p0, p1)	{ \
							.cmd_type = SHORT_CMD_MIPI, \
							.pkt.escape_or_hs = DPHY_ESCAPE, \
							.pkt.ph.di = dt, \
							.pkt.ph.packet_data.sp.data0 = p0, \
							.pkt.ph.packet_data.sp.data1 = p1, \
						}

#define DLY_MS(ms)      { \
							.cmd_type = DSI_DELAY, \
							.delay_ms = ms, \
						}

#define LP(dt, ptr)     { \
							.cmd_type = LONG_CMD_MIPI, \
							.pkt.escape_or_hs = DPHY_ESCAPE, \
							.pkt.ph.di = dt, \
							.pkt.ph.packet_data.lp.wc = ARRAY_SIZE(ptr), \
							.pkt.pPayload = ptr, \
						}

